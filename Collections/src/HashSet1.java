import java.util.HashSet;

public class HashSet1 {

	public static void main(String[] args) {
		HashSet hs = new HashSet();
		//Hashset follows hashing algorithm. it is combination of hashTable and hash function
		hs.add(100);
		hs.add(50);
		hs.add(150);
		hs.add(25);
		hs.add(125);
		
		System.out.println(hs);

	}

}
